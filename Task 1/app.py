import sys

# Taking input form the command-line arguments, as two integers
a = int(sys.argv[1])
b = int(sys.argv[2])

# Calculating and printing the sum, difference, product, and division result
print("The sum is: ", a + b)
print("The difference is: ", a - b)
print("The product is: ", a * b)
print("The divided answer is: ", a / b)
